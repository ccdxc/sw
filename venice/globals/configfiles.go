package globals

const (

	// RegistryConfigFile is used to store the docker registry
	RegistryConfigFile = "/etc/pensando/shared/common/registry.conf"

	// FilebeatConfigFile is used by Filebeat and it contains config params
	FilebeatConfigFile = "/etc/pensando/" + Filebeat + "/filebeat.yml"

	// FilebeatFieldsFile is used by Filebeat and it contains logging fields
	// mapping for elasticsearch indexing.
	FilebeatFieldsFile = "/etc/pensando/" + Filebeat + "/logging_fields.yml"

	// ElasticDiscoveryConfigFile is used by Elastic for Node discovery
	ElasticDiscoveryConfigFile = ElasticConfigDir + "/elastic-discovery/unicast_hosts.txt"

	// ElasticMgmtConfigFile is used by Elastic for binding to MgmtAddr and publishing to peers
	ElasticMgmtConfigFile = ElasticConfigDir + "/mgmt_env.sh"

	// ElasticDataVolumeDir is used by Elastic for storing indexed data
	ElasticDataVolumeDir = "/var/lib/pensando/" + ElasticSearch + "/elasticsearch/data"

	// EtcdConfigFile is written by CMD when starting cluster
	EtcdConfigFile = "/etc/pensando/" + Etcd + "/etcd.conf"

	// APIServerConfigFile is used by ApiServer
	APIServerConfigFile = "/etc/pensando/" + APIServer + "/apiserver.conf"

	// NtpConfigFile is generated by NTP service for chronyd format
	NtpConfigFile = "/etc/pensando/" + Ntp + "/chrony.conf"

	// InfluxConfigFile is used by Influx service
	InfluxConfigFile = "/etc/pensando/" + Influx + "/influxdb.conf"

	// NaplesModeConfigFile is where nmd writes mode information for other processes on naples to pickup. This is written on the penctl mode change trigger.
	NaplesModeConfigFile = "/sysconfig/config0/device.conf"

	// NmdDBPath is where nmd defaults to writing its db file. This needs to be in non tmpfs partition
	NmdDBPath = "/sysconfig/config0/nmd.db"

	// NetAgentDBPath is where netagent defaults to writing its db file. This needs to be in non tmpfs partition
	NetAgentDBPath = "/sysconfig/config0/pen-netagent.db"

	// NaplesModeBackupConfigFile is where nmd writes mode information for other processes on naples to pickup. This is written on the penctl mode change trigger.
	NaplesModeBackupConfigFile = "/sysconfig/config1/device.conf"

	// NmdBackupDBPath is where nmd defaults to writing its db file. This needs to be in non tmpfs partition
	NmdBackupDBPath = "/sysconfig/config1/nmd.db"

	// IotaEmulation is the which is used to indicate to NMD that it is running in Emulation mode without a real Venice Controller
	IotaEmulation = "/data/iota-emulation"
)

var (
	// NaplesTrustRootsFile is the location of the file containing Venice trust roots on NAPLES
	NaplesTrustRootsFile = "/sysconfig/config0/clusterTrustRoots.pem"

	// NaplesTrustRootsBackupFile is the location of the file containing backup of the Venice trust roots on NAPLES
	NaplesTrustRootsBackupFile = "/sysconfig/config1/clusterTrustRoots.pem"

	// NaplesConfig is the directory where config files for Naples are kept
	NaplesConfig = "/sysconfig/config0"

	// NaplesConfigBackup is the directory where all the config files are backed up
	NaplesConfigBackup = "/sysconfig/config1"
)
